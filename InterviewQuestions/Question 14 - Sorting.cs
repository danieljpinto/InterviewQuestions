using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace InterviewQuestions
{
    /// <summary>
    /// Without using any kind of .Sort or OrderBy Method or touching the constructor (other than one you make yourself) re-arrange the Numbers list into numerical order
    /// Hint: I'm looking for loops
    /// </summary>
    public class Question14
    {
        public List<int> Numbers { get; set; }

        public Question14()
        {
            Numbers = new List<int>()
            {
                1,9,32,5,2,6,4,2,7
            };    
        }

        public void Answer()
        {
            for (int i = 0; i < Numbers.Count; i++)
            {
                for (int j = 0; j < Numbers.Count; j++)
                {
                    if (Numbers[i] > Numbers[j])
                    {
                        var temp = Numbers[j];
                        Numbers[j] = Numbers[i];
                        Numbers[i] = temp;
                    }
                }
            }
        }
    }
}
